# Load all vars from .env.shared into environment without executing values.
# Some vault values are command strings with spaces; treating the vault as a
# shell script can accidentally execute those strings during bootstrap.
eval "$(
  python3 - "$SHARED_ENV" <<'PY'
import re
import shlex
import sys
from pathlib import Path

env_path = Path(sys.argv[1])
for raw in env_path.read_text(encoding="utf-8", errors="ignore").splitlines():
    line = raw.strip()
    if not line or line.startswith("#") or "=" not in line:
        continue
    key, value = line.split("=", 1)
    key = key.strip()
    if not re.fullmatch(r"[A-Za-z_][A-Za-z0-9_]*", key):
        continue
    value = value.strip()
    if len(value) >= 2 and value[0] == value[-1] and value[0] in {"'", '"'}:
        value = value[1:-1]
    print(f"export {key}={shlex.quote(value)}")
PY
)"

REPO="${1:-}"
TARGET="${2:-}"

if [[ -z "$REPO" ]]; then
  echo "Usage: $0 <repo> [target-path]" >&2
  echo "Repos: hp-staging, hp-worktree, hp-frontend, ais, lam, knocktracker, governance, seek, seek-local, seek-worktree, stampede" >&2
  exit 1
fi

write_env() {
  local target="$1"
  local content="$2"
  if [[ "${DRY_RUN:-0}" == "1" ]]; then
    echo "=== DRY RUN: would write to $target ==="
    while IFS= read -r line; do
      if [[ "$line" =~ ^[A-Za-z_][A-Za-z0-9_]*= ]]; then
        local key="${line%%=*}"
        local value="${line#*=}"
        if [[ -n "$value" ]]; then
          printf '%s=<redacted>\n' "$key"
        else
          printf '%s=\n' "$key"
        fi
      else
        printf '%s\n' "$line"
      fi
    done <<< "$content"
  else
    mkdir -p "$(dirname "$target")"
    printf '%s\n' "$content" > "$target"
    echo "Wrote $target"
  fi
}

