#   seek-local      seek-and-ponder/.env  (local Supabase)
#   seek-worktree   Requires explicit target-path  e.g. .../.env
#   stampede        Stampede/.env  (default)
#
# Dry-run: set DRY_RUN=1 to print a redacted output preview without writing.
#
# Examples:
#   bootstrap-repo-env.sh hp-staging
#   bootstrap-repo-env.sh hp-worktree /path/to/worktree/backend/.env.local
#   DRY_RUN=1 bootstrap-repo-env.sh hp-staging

